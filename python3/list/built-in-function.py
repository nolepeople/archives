
List = [1,2,3,4,5,6,7]

# len() : memberikan total panjang list
print (len(List))

# max() : mengembalikan item dari list dengan nilai maks
# hanya berlaku untuk item yang mempunyai nilai integer
print (max(List))

# min() : mengembalikan item dari list dengan nilai min
print (min(List))

# mengubah tuple menjadi list
print (list((1,2,3,4,5)))



